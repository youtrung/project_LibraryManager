    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:        
       int click = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa ?");
       int index=tblDocgia.getSelectedRow();
       if(index <0)
       {
           JOptionPane.showMessageDialog(getContentPane(), "hãy chọn trước khi xóa");
       }
       else if (click == 0) {
            POJO_DOCGIA dg=tblmodelDG.getByIndex(tblDocgia.getSelectedRow());
            if(DAO_DOCGIA.checkDieuKien(dg.getMADG())==true)
            {
                JOptionPane.showMessageDialog(null,"Đọc giả này đang mượn sách ,không thể xóa");
            }
            else 
            {
            tblmodelDG.removeElement(dg);
            DAO_DOCGIA.xoaDocgia(dg.getMADG());  
            }
            
       }
        
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int index=tblDocgia.getSelectedRow();
        if(index<0)
        {
            JOptionPane.showMessageDialog(null,"Hãy chọn bảng dữ liệu cần sửa");
            return ;
        }
        POJO_DOCGIA dg=tblmodelDG.getByIndex(index);
        dg.setTENDG(txtTenDG.getText());
        dg.setNGAYSINH(txtNgaysinh.getText());
        dg.setDIACHI(txtDiachi.getText());
        dg.setEMAIL(txtEmail.getText());
        DAO_DOCGIA.UpdateTenDocGia(dg);
        DAO_DOCGIA.UpdateNgaySinhDocGia(dg);
        DAO_DOCGIA.UpdateDiaChiDocGia(dg);
        DAO_DOCGIA.UpdateEmailDocGia(dg);
        tblmodelDG.reloadTbl();
        
    }//GEN-LAST:event_btnSuaActionPerformed

