    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        
        String madg=txtMaDG.getText();
        String tendg=txtTenDG.getText();
      
        //kiem tra dinh dang ngay thang
        Pattern patternDate =Pattern.compile("^\\d{2}[-|/]\\d{2}[-|/]\\d{4}$");
        String diachi=txtDiachi.getText();
        String email=txtEmail.getText();
        String ngaysinh=txtNgaysinh.getText();
        
        //kiem tra dinh dang email
        Pattern patternEmail=Pattern.compile("^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$");
        POJO_DOCGIA dg= new POJO_DOCGIA();
        if (txtMaDG.getText().isEmpty()==true)
        {
            JOptionPane.showMessageDialog(this,"Không được để trống Mã đọc giả ","Thông báo",JOptionPane.ERROR_MESSAGE);
        }
        else if(madg.length()>=5)
        {
            JOptionPane.showMessageDialog(this,"Mã đọc giả dài quá 4 đơn vị  ","Thông báo",JOptionPane.ERROR_MESSAGE);
        }
        else if (patternEmail.matcher(email).matches()==false && email.isEmpty()==false )
        {
            JOptionPane.showMessageDialog(this,"Sai định dạng Email ","Thông báo",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            dg.setMADG(madg);
            dg.setTENDG(tendg);
            dg.setNGAYSINH(ngaysinh);
            dg.setDIACHI(diachi);  
        tblmodelDG.addElement(dg);
        DAO_DOCGIA.themDocGia(dg);
        JOptionPane.showMessageDialog(this,"Thêm đọc giả thành công ","Thông báo",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

